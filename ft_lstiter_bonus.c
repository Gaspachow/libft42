#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	tmp = lst;
	while (tmp)
	{
		(*f)((void *)tmp->content);
		tmp = tmp->next;
	}
}
